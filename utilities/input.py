#-Function to read maps and float input
def input(self, config, pcr, name, module, variable, empty = None):
    try:
        setattr(self, name, self.ones * config.getfloat(module, variable))
    except:
        try:
            setattr(self, name, pcr.readmap(self.inpath + config.get(module, variable)))
        except:
            setattr(self, name, self.ones * empty)
